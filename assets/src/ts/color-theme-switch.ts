// This script has been adapted to work with the Geoluminate theme
import { dom, library } from '@fortawesome/fontawesome-svg-core'
import { faCircleHalfStroke, faMoon, faSun } from '@fortawesome/free-solid-svg-icons'

// Add the imported solid icons to the library
library.add(faSun, faMoon, faCircleHalfStroke)

dom.watch();


(() => {
  'use strict'

  const storedTheme = localStorage.getItem('theme') as string

  const getPreferredTheme = () => {
    if (storedTheme) {
      return storedTheme
    }

    return window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light'
  }

  const setTheme = function (theme: string) {
    if (theme === 'auto' && window.matchMedia('(prefers-color-scheme: dark)').matches) {
      document.documentElement.setAttribute('data-bs-theme', 'dark')
    } else {
      document.documentElement.setAttribute('data-bs-theme', theme)
    }
  }

  setTheme(getPreferredTheme())

  const showActiveTheme = (theme: string, focus = false) => {
    const themeSwitcher = document.querySelector('#bd-theme') as HTMLElement

    if (!themeSwitcher) {
      return
    }
    const btnToActive = document.querySelector(`[data-bs-theme-value="${theme}"]`) as HTMLElement
    document.querySelectorAll('[data-bs-theme-value]').forEach(element => {
      element.classList.remove('active')
      element.setAttribute('aria-pressed', 'false')
    })

    btnToActive.classList.add('active')
    btnToActive.setAttribute('aria-pressed', 'true')

    if (focus) {
      themeSwitcher.focus()
    }
  }

  window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', () => {
    if (!storedTheme ? 'dark' : "light") {
      // if (storedTheme !== 'light' || storedTheme !== 'dark') {
      setTheme(getPreferredTheme())
    }
  })

  window.addEventListener('DOMContentLoaded', () => {
    showActiveTheme(getPreferredTheme())

    document.querySelectorAll('[data-bs-theme-value]')
      .forEach(toggle => {
        toggle.addEventListener('click', () => {
          const theme = toggle.getAttribute('data-bs-theme-value')
          localStorage.setItem('theme', theme)
          setTheme(theme)
          showActiveTheme(theme, true)
        })
      })
  })
})()
